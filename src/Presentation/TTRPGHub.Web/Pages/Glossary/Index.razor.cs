    private string _searchQuery = string.Empty;
    private string _selectedCategory = string.Empty;
    private char? _selectedLetter;

    protected string SearchQuery
    {
        get => _searchQuery;
        set { _searchQuery = value; }
    }

    protected string SelectedCategory
    {
        get => _selectedCategory;
        set { _selectedCategory = value; }
    }

    protected char? SelectedLetter
    {
        get => _selectedLetter;
        set { _selectedLetter = value; }
    }

    protected IReadOnlyList<string> AllCategories { get; } =
        Terms.Select(t => t.Category).Distinct().Order().ToList();

    protected IReadOnlyList<char> AllLetters { get; } =
        Terms.Select(t => t.Name[0]).Distinct().Order().ToList();

    protected IEnumerable<GlossaryTerm> FilteredTerms =>
        Terms
            .Where(t => string.IsNullOrEmpty(_searchQuery) ||
                        t.Name.Contains(_searchQuery, StringComparison.OrdinalIgnoreCase) ||
                        t.Description.Contains(_searchQuery, StringComparison.OrdinalIgnoreCase) ||
                        (t.Alt != null && t.Alt.Contains(_searchQuery, StringComparison.OrdinalIgnoreCase)))
            .Where(t => string.IsNullOrEmpty(_selectedCategory) || t.Category == _selectedCategory)
            .Where(t => _selectedLetter == null || t.Name[0] == _selectedLetter)
            .OrderBy(t => t.Name);

