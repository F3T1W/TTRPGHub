using Microsoft.Extensions.Logging;
using TTRPGHub.Entities;
using TTRPGHub.Repositories;

namespace TTRPGHub.Seeding;

// Гайды и памятки — системно-независимый контент (Category=Rule, не привязан к конкретной системе),
// поэтому переиспользует уже готовую инфраструктуру справочника (RuleEntry + Markdig-рендер на
// /reference/{system}/rules/{category}/{slug}) вместо отдельного движка статей. Официальная
// системная запись "guides" — не игровая система, а просто контейнер для лонгридов, см. ROADMAP.md.
public sealed class GuidesSeeder(
    IGameSystemRepository systemRepository,
    IRuleEntryRepository entryRepository,
    IUnitOfWork unitOfWork,
    ILogger<GuidesSeeder> logger)
{
    public async Task SeedIfEmptyAsync(CancellationToken ct = default)
    {
        var system = await systemRepository.GetBySlugAsync("guides", ct);
        if (system is null)
        {
            system = GameSystem.CreateOfficial("guides", "Гайды и памятки");
            await systemRepository.AddAsync(system, ct);
            await unitOfWork.SaveChangesAsync(ct);
            logger.LogInformation("Создана системная запись 'guides' для онбординг-контента");
        }

        if (await entryRepository.AnyAsync(system.Id, RuleCategory.Rule, ct))
            return;

        var guides = new[]
        {
            RuleEntry.Create(
                system.Id, RuleCategory.Rule, "session-zero", "Сессия-ноль: как подготовить",
                summary: "Встреча перед стартом кампании — про что договориться, прежде чем начнётся история.",
                contentMarkdown: SessionZeroContent, statsJson: "{}",
                tags: ["мастеру", "онбординг"], isHomebrew: false, source: "Таверна Аферистов"),

            RuleEntry.Create(
                system.Id, RuleCategory.Rule, "gm-first-session", "Памятка новому мастеру: первая сессия",
                summary: "Чек-лист подготовки и шпаргалка на случай, если игроки пойдут не туда, куда планировалось.",
                contentMarkdown: GmFirstSessionContent, statsJson: "{}",
                tags: ["мастеру", "онбординг"], isHomebrew: false, source: "Таверна Аферистов"),

            RuleEntry.Create(
                system.Id, RuleCategory.Rule, "player-basics", "Базовые механики НРИ для нового игрока",
                summary: "Броски кубиков, проверки характеристик, инициатива и что вообще происходит за столом.",
                contentMarkdown: PlayerBasicsContent, statsJson: "{}",
                tags: ["игроку", "онбординг"], isHomebrew: false, source: "Таверна Аферистов"),
        };

        await entryRepository.AddRangeAsync(guides, ct);
        await unitOfWork.SaveChangesAsync(ct);
        logger.LogInformation("Добавлены онбординг-гайды: {Count}", guides.Length);
    }

    private const string SessionZeroContent = """
        Сессия-ноль — это встреча перед первой игровой сессией кампании, где ещё не бросают
        кубики и не сражаются с монстрами. Здесь договариваются о правилах игры за столом,
        а не только о правилах системы.

        ## Зачем она нужна

        Кампания может идти месяцами или годами. Гораздо дешевле десять минут поговорить
        о границах и ожиданиях заранее, чем на пятой сессии выяснить, что кому-то было
        некомфортно от сцены пытки или что один игрок хотел героическую историю, а другой —
        мрачный гримдарк.

        ## Что обсудить

        - **Сеттинг и тон.** Тёмное фэнтези или лёгкое приключение? Высокий уровень магии
          или низкий? Насколько серьёзно всё воспринимается за столом?
        - **Границы (session zero safety tools).** Какие темы точно не будут подниматься
          (насилие над детьми, сексуальное насилие и т.д.) и какие — только с предупреждением
          заранее. Полезный инструмент — «карточка X»: игрок молча показывает карту X,
          и сцена сворачивается без объяснений.
        - **Ожидания по частоте игр.** Раз в неделю, раз в две недели, нерегулярно?
          Что делать, если кто-то не может прийти — переносить сессию или играть без него?
        - **Формат.** Онлайн или оффлайн, где и когда, нужен ли войсчат или текст.
        - **Персонажи.** Мастер объясняет, какая пати нужна для задуманной истории
          (например, всем нужна причина быть вместе с первой сцены), игроки — что им
          интересно играть. Полезно строить предыстории персонажей так, чтобы они
          зацепляли друг друга.
        - **Хоумрулы.** Если мастер меняет какие-то правила системы — сказать об этом сразу,
          а не когда правило внезапно всплывёт в бою.

        ## Практический совет

        Не обязательно делать сессию-ноль отдельной встречей — если группа маленькая и все
        друг друга хорошо знают, достаточно 15–20 минут перед первой полноценной сессией.
        Но пропускать совсем не стоит: правило «мы же и так все друг друга знаем» — самый
        частый источник конфликтов за столом.
        """;

    private const string GmFirstSessionContent = """
        Первая сессия в роли мастера — это нормально бояться. Все ведущие через это проходили.
        Вот чек-лист, который снимает часть тревоги подготовкой.

        ## За день до игры

        - [ ] Перечитай заметки с прошлой сессии (если это не первая игра в кампании) —
          открой [заметки сессии](/sessions) в разделе своих кампаний.
        - [ ] Накидай 2–3 варианта развития событий. Игроки почти никогда не идут
          по единственному предсказанному пути — не пиши сценарий как книгу,
          готовь ситуации и реагируй на решения игроков.
        - [ ] Подготовь монстров/NPC заранее — открой [бестиарий](/reference/dnd5e/monsters)
          и выпиши статы тех, кто может понадобиться, чтобы не листать книгу посреди боя.
        - [ ] Проверь, у всех ли игроков готовы персонажи (лист, начальное снаряжение).

        ## Прямо перед стартом

        - [ ] Напомни границы, обговорённые на сессии-ноль (см. [гайд по сессии-ноль](/reference/guides/rules/rule/session-zero)).
        - [ ] Кратко резюмируй, что произошло в прошлый раз — даже если все помнят,
          это настраивает на нужный тон.
        - [ ] Если ведёшь за виртуальным столом — открой сессию и проверь кубики/музыку заранее,
          не при игроках.

        ## Если игроки пошли не туда, куда планировалось

        Это будет случаться постоянно, и это хорошо — значит, у игроков есть агентность.
        Несколько приёмов:

        - **Переиспользуй заготовки.** NPC, которого ты написал для одной сцены, легко
          переставить в другую локацию — игроки не узнают разницы.
        - **«Да, и...» вместо «нет».** Вместо того чтобы блокировать нежданное решение
          игроков, разверни его в новую интересную ветку.
        - **Возьми паузу.** Пять минут перерыва — нормально, чтобы сориентироваться
          и прикинуть, что будет дальше. Никто не ждёт, что мастер держит весь сюжет в голове
          без сучка и задоринки.
        - **Не бойся сказать «дай мне подумать, вернёмся к этому через пять минут».**
          Это честнее и лучше для игры, чем импровизировать наугад важное решение.

        ## После сессии

        - [ ] Запиши коротко, что произошло — пригодится через месяц, когда всё забудется.
          Используй [заметки сессии](/sessions) в кампании — они видны только участникам.
        - [ ] Отметь, какие NPC/локации/сюжетные нити остались открытыми.

        Веди игру, чтобы всем — включая тебя — было весело. Правила существуют, чтобы
        обслуживать историю, а не наоборот.
        """;

    private const string PlayerBasicsContent = """
        Если ты никогда не играл в настольную ролевую игру (НРИ / TTRPG) — вот минимум,
        который нужно знать перед первой сессией. Подробные термины смотри в [словаре](/glossary).

        ## Что вообще происходит за столом

        Мастер (ведущий, ГМ) описывает мир и играет всех персонажей, кроме твоего.
        Ты говоришь, что делает твой персонаж. Когда результат действия неочевиден —
        бросаешь кубики, чтобы узнать, получилось ли.

        ## Броски кубиков

        Базовый кубик большинства систем — двадцатигранный (d20, «дэ-двадцать»).
        Формула проверки обычно выглядит так:

        ```
        d20 + модификатор характеристики + бонус мастерства (если владеешь навыком) ≥ Сложность (СЛ)
        ```

        Например, чтобы взломать замок: бросаешь d20, прибавляешь модификатор Ловкости
        и (если владеешь инструментами взломщика) бонус мастерства. Мастер сравнивает
        результат со Сложностью, которую сам определил заранее (не всегда озвучивает её).

        ## Модификаторы характеристик

        У персонажа шесть характеристик (Сила, Ловкость, Телосложение, Интеллект, Мудрость,
        Харизма), у каждой — значение (обычно 8–18 на старте) и модификатор, который
        реально участвует в бросках:

        | Значение характеристики | Модификатор |
        |---|---|
        | 8–9 | −1 |
        | 10–11 | +0 |
        | 12–13 | +1 |
        | 14–15 | +2 |
        | 16–17 | +3 |
        | 18–19 | +4 |

        Мастер создания персонажа на сайте считает это [автоматически](/characters/create-wizard) —
        запоминать формулу необязательно.

        ## Инициатива и бой

        В начале боя каждый бросает инициативу (d20 + модификатор Ловкости обычно) —
        это определяет порядок ходов. На своём ходу можно подойти, атаковать, применить
        способность или сделать что-то ещё — мастер подскажет, что доступно. На сайте есть
        [трекер инициативы](/sessions), который сам считает очерёдность.

        ## Хиты (HP)

        Здоровье персонажа — «очки хитов» (HP). Когда HP падает до нуля, персонаж
        обычно не сразу умирает (в большинстве систем есть бросок «на смерть»
        или похожий механизм) — мастер объяснит, как это работает в конкретной системе.

        ## Самое важное

        Не обязательно знать все правила наизусть перед первой игрой. Спрашивай мастера —
        это нормально и ожидаемо. Твоя задача — представлять, что делает персонаж,
        и получать удовольствие от истории, которую вы сочиняете вместе.
        """;
}
